import imageGeneratorService from '../services/imageGenerator.service.js';
import ContentPlan from '../models/contentPlan.model.js';
import ImageBrief from '../models/imageBrief.model.js';
import ImagePrompt from '../models/imagePrompt.model.js';
import GeneratedImage from '../models/generatedImage.model.js';
import SceneStoryboard from '../models/sceneStoryboard.model.js';
import AppError from '../utils/appError.js';

const catchAsync = (fn) => (req, res, next) => {
  fn(req, res, next).catch(next);
};

export const generateImages = catchAsync(async (req, res, next) => {
  console.log(`[ImageController] generateImages called. Request Body:`, JSON.stringify(req.body, null, 2));
  let { contentPlanId, prompts } = req.body;

  if (!contentPlanId) {
    const latestPlan = await ContentPlan.findOne().sort({ createdAt: -1 });
    if (!latestPlan) {
      console.warn(`[ImageController] No content plans found.`);
      return next(new AppError('No content plans found. Please generate a content plan first.', 404));
    }
    contentPlanId = latestPlan._id;
  }

  console.log(`[ImageController] Querying storyboard scenes for contentPlanId: ${contentPlanId}`);
  const scenes = await SceneStoryboard.find({ contentPlanId }).sort({ slideNumber: 1 });
  console.log(`[ImageController] Storyboard scenes loaded from MongoDB (Count: ${scenes.length}):`, JSON.stringify(scenes, null, 2));

  if (scenes.length === 0) {
    if (prompts && Array.isArray(prompts)) {
      console.log(`[ImageController] No scenes found, but raw prompts fallback provided:`, prompts);
      const images = await imageGeneratorService.generateImages(prompts);
      return res.status(200).json(images);
    }
    console.warn(`[ImageController] No storyboard scenes found for plan: ${contentPlanId}`);
    return next(new AppError('No storyboard scenes found for this content plan. Please generate storyboard scenes first.', 404));
  }

  // Verify that the Image Generator uses the exact prompt field (scene.imagePrompt) generated by Scene Storyboard
  console.log(`[ImageController] Verifying prompt mappings:`);
  scenes.forEach((s, idx) => {
    console.log(`  - Scene #${s.slideNumber || idx+1} prompt mappings: s.imagePrompt = "${s.imagePrompt || 'N/A'}", s.visualIdea = "${s.visualIdea || 'N/A'}"`);
  });

  try {
    const images = await imageGeneratorService.generateImages(scenes);
    console.log(`[ImageController] Image generation completed successfully. Returning ${images.length} images.`);
    res.status(200).json(images);
  } catch (error) {
    console.error(`[ImageController Error] generateImages caught error:`, error);
    // Explicitly return a detailed JSON error instead of generic 500
    res.status(500).json({
      status: 'error',
      message: error.message,
      stack: error.stack,
      error
    });
  }
});

export const getImages = catchAsync(async (req, res, next) => {
  const images = await GeneratedImage.find().sort({ createdAt: -1 }).populate('sceneId');
  const mappedImages = images.map(img => {
    const obj = img.toObject();
    obj.prompt = img.sceneId?.imagePrompt || '';
    return obj;
  });
  res.status(200).json(mappedImages);
});

export const deleteImage = catchAsync(async (req, res, next) => {
  const { id } = req.params;
  const img = await GeneratedImage.findByIdAndDelete(id);
  if (!img) return next(new AppError('Image not found.', 404));
  res.status(200).json({ success: true, message: 'Image deleted successfully.' });
});

export const getLatestImages = catchAsync(async (req, res, next) => {
  const latestPlan = await ContentPlan.findOne().sort({ createdAt: -1 });
  if (!latestPlan) {
    return next(new AppError('No content plans found.', 404));
  }

  const scenes = await SceneStoryboard.find({ contentPlanId: latestPlan._id }).sort({ slideNumber: 1 });
  const sceneIds = scenes.map(s => s._id);

  const images = await GeneratedImage.find({ sceneId: { $in: sceneIds } }).sort({ createdAt: 1 }).populate('sceneId');

  const mappedImages = images.map(img => {
    const obj = img.toObject();
    obj.prompt = img.sceneId?.imagePrompt || '';
    return obj;
  });

  res.status(200).json({
    success: true,
    contentPlanId: latestPlan._id,
    images: mappedImages
  });
});

